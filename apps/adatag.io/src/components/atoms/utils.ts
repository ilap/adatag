export function walletColors(name: string): string {
  switch (name) {
    case 'eternl':
      return '#F2C374'
    case 'Flint Wallet':
      return '#F2A9CE'
    case 'lace':
      return '#FC8D94'
    case 'Nami':
      return '#AFCCA1'
    case 'NuFi':
      return '#79A6FC'
    case 'GeroWallet':
      return '#9DDDD5'
    case 'Typhon Wallet':
      return '#EDB37E'
    case 'yoroi':
      return '#FFAA99'

    default:
      return '#F2C374'
  }
}
